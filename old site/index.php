							<h1>What's my story?</h1>
							<p>
							Born and raised in rural Ohio, I grew up spending a lot of time outdoors. Some of my favorite activities as a child (in clear contrast to my hobbies today) included fishing, insect collecting, and raising my goats and ducks. My fascination and soon-to-be obsession with creating technology started the summer after graduating high school. This is when I began what was my first real entrepreneurial endeavor. I have had a love for the weather since I was in grade school, and so I knew that I wanted to make something with that passion. The Snow Day Calculator, a 99 cent iPhone app that calculated whether or not you should do your homework before a winter storm, sold nearly 1,000 copies and was featured on the evening news in the three largest cities in Ohio. Since then, I've never looked back. After getting to college in Virginia, I've been lucky enought to have started several great companies and work on countless hacks.  I've worked on projects mainly in the realm of social media as I have a certain passion for creating things that make value and are addicting for people to use.

